export function calculatePathway(scores) {
  if (scores.interactionScore <= 2 || scores.regulationScore <= 2) {
    return "interaction";
  }
  if (scores.understandingScore <= 2 || scores.requestScore <= 2) {
    return "understanding";
  }
  if (scores.speechScore <= 3) {
    return "firstWords";
  }
  return "wordCombination";
}
