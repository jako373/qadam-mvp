function buildInstruction(language, source, categoryIndex, title) {
  const category = exerciseCategories[source.category][language];
  const guidance = exerciseGuidance[source.category][language];
  const material = guidance.materials[categoryIndex];
  const parentWords = guidance.words[categoryIndex];
  const wait = language === "kk"
    ? "Сөзді бір рет айтыңыз да, 5 секунд үнсіз күтіңіз."
    : "Скажите фразу один раз и молча подождите 5 секунд.";
  const help = language === "kk"
    ? "Бала бастамаса, әрекетті бір рет көрсетіңіз немесе қолын жай ғана бағыттаңыз; өзі аяқтауына мүмкіндік беріңіз."
    : "Если ребёнок не начал, один раз покажите действие или мягко направьте руку; дайте закончить самому.";
  const repeatPlan = language === "kk"
    ? `Бір айналымда ${source.level === 1 ? "2-3" : "3-4"} рет қайталаңыз. Күніне 1 рет, барлығы ${source.durationMinutes} минут жеткілікті.`
    : `Повторите ${source.level === 1 ? "2-3" : "3-4"} раза за один подход. Достаточно 1 раза в день, всего ${source.durationMinutes} минут.`;
  const benefit = language === "kk"
    ? `Бұл жаттығу «${category.goal}» бағытын қолдайды. Қысқа әрі тұрақты қайталау баланың осы дағдыны күнделікті жағдайда қолдануына көмектеседі.`
    : `Упражнение поддерживает направление «${category.goal}». Короткое регулярное повторение помогает ребёнку использовать навык в повседневной ситуации.`;
  const stopRule = language === "kk"
    ? "Бала теріс бұрылса, материалды итерсе немесе мазасызданса, бірден тоқтаңыз. Кейінірек қысқа әрі жеңіл нұсқамен қайталап көріңіз."
    : "Сразу остановитесь, если ребёнок отворачивается, отталкивает материал или начинает беспокоиться. Попробуйте позже в более коротком и простом варианте.";

  return {
    title,
    goal: category.goal,
    materials: [material],
    preparation: setupSteps[language][source.category],
    parentWords,
    steps: [setupSteps[language][source.category], `«${parentWords}» ${wait}`, help],
    repeatPlan,
    successCriteria: category.success,
    easierVersion: category.easier,
    harderVersion: category.harder,
    benefit,
    parentTip: category.tip,
    stopRule,
  };
}

export function localizeExercise(source, categoryIndex) {
  const russianTitle = russianTitles[source.category][categoryIndex];
  return {
    id: source.id,
    category: source.category,
    level: source.level,
    durationMinutes: source.durationMinutes,
    tags: source.tags,
    isActive: source.isActive,
    kk: buildInstruction("kk", source, categoryIndex, source.title),
    ru: buildInstruction("ru", source, categoryIndex, russianTitle),
  };
}
