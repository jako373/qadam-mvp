export function localizeExercise(source, categoryIndex) {
  const category = exerciseCategories[source.category];
  const russianTitle = russianTitles[source.category][categoryIndex];
  const kkMaterials = source.materials.length ? source.materials : [category.kk.material];
  return {
    id: source.id,
    category: source.category,
    level: source.level,
    durationMinutes: source.durationMinutes,
    tags: source.tags,
    isActive: source.isActive,
    kk: {
      title: source.title,
      goal: source.goal,
      materials: kkMaterials,
      steps: source.steps,
      successCriteria: category.kk.success,
      easierVersion: category.kk.easier,
      harderVersion: category.kk.harder,
      parentTip: category.kk.tip,
    },
    ru: {
      title: russianTitle,
      goal: category.ru.goal,
      materials: [category.ru.material],
      steps: russianSteps(source.category, russianTitle),
      successCriteria: category.ru.success,
      easierVersion: category.ru.easier,
      harderVersion: category.ru.harder,
      parentTip: category.ru.tip,
    },
  };
}
